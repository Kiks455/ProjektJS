class Recording{
    constructor(id, title, length, releaseDate, tags, artists, rating, comments){
        this.id=id;
        this.title=title;
        this.length=length;
        this.releaseDate=releaseDate;
        this.tags=tags;
        this.artists=artists;
        this.rating=rating;
        this.comments=comments;
    }
}

export default Recording;